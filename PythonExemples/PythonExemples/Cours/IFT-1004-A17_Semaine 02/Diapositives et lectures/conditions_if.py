# Description: Les structures conditionnelles (La forme if)

# Exemple 1
n = 10

if n % 2 == 0:
    print('n est un nombre pair.')

# Exemple 2
n = 5

if n % 2 == 0:
    print('n est un nombre pair.')

# Exemple 3
n = 5

if n >= 2:
    print('n est plus grand que 2.')

# Exemple 4 
n = 2

if n <= 2:
    print('n est inférieur ou égal à 2.')
