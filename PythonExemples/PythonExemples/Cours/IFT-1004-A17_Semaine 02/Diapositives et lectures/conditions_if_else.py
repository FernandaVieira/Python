# Description: Les structures conditionnelles (La forme if-else)

# Exemple 1
n = 5

if n % 2 == 0:
    print('n est un nombre pair.')
else:
    print('n est un nombre impair.')

# Exemple 2 (Syntaxe élégante)
n = 5

if 4 <= n <= 6:
    print('n est compris entre 4 et 6.')
else:
    print("n n'est pas compris entre 4 et 6.")
